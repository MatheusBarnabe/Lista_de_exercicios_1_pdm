function soma(a, b) {
    return a + b;
}

function subtracao(a, b) {
    return a - b;
}

function mult (a, b) {
    return a * b;
}

function div (a, b) {
    return a / b;
}

const res1 = soma (10, 5)
const res2 = subtracao (10, 5) 
const res3 = mult (10, 5)
const res4 = div (10, 5)

console.log(`10 + 5 = ` + res1)
console.log(`10 - 5 = ` + res2)
console.log(`10 * 5 = ` + res3)
console.log(`10 / 5 = ` + res4)
