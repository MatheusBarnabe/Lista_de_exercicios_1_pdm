const soma = (a, b) => {
    return a + b;
}

const subtracao = (a, b) => {
    return a - b;
}

const mult = (a, b) => {
    return a * b;
}

const div = (a, b) => {
    return a / b;
}

console.log(`10 + 5 = ` + soma(10, 5))
console.log(`10 - 5 = ` + subtracao(10, 5))
console.log(`10 * 5 = ` + mult(10, 5))
console.log(`10 / 5 = ` + div(10, 5))
